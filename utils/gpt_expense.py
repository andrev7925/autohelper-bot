import openai
from config import OPENAI_API_KEY

# Приклад: отримані з API або словника
fuel_price = 1.7  # €/л
insurance = 1100  # €/рік
tax = 350         # €/рік
maintenance = 500 # €/рік

async def gpt_expense_analysis(car, country):

    prompt = f"""
УВАГА! Всі розрахунки і всі дані — тільки для країни {country}!
Якщо країна не {country}, не відповідай. Якщо країна {country} — всі ціни, податки, страховка, сервіс, поломки, транспорт, попит — тільки для {country} станом на 2024–2025 рік.

Ти — експерт з авто та ринку кожної країни. На основі цих даних про авто:
Марка: {car.get('brand', '')}
Модель: {car.get('model', '')}
Рік: {car.get('year', '')}
Ціна: {car.get('price', '')}
Витрата пального: {car.get('fuel_consumption', '')}
Тип пального: {car.get('fuel_type', '')}
Пробіг: {car.get('mileage', '')}
Країна: {country}

Використовуй такі актуальні ціни для {country}:
- Паливо: {fuel_price} €/л
- Страхування: {insurance} €/рік
- Податок: {tax} €/рік
- Обслуговування: {maintenance} €/рік
Всі розрахунки роби для країни {country} станом на 2025 рік.
Використовуй свої найактуальніші знання про ціни на паливо, страховку, податки, сервіс, амортизацію, типові поломки саме для цієї країни (навіть якщо ціни змінилися нещодавно). Якщо не впевнений — вкажи, що ціна приблизна, і не занижуй її. Не використовуй середньосвітові ціни, а тільки для країни {country}.



Для амортизації використовуй такі ставки:
- 1–3 роки: 10–15%/рік
- 3–5 років: 8–12%/рік
- 5–10 років: 5–8%/рік
- 10+ років: 2–5%/рік

Розрахуй витрати на рік для цього авто, враховуючи ці параметри.

Зроби аналіз у такому форматі (заповни всі змінні):

📟 Калькулятор витрат для {car.get('brand', '')} {car.get('model', '')} {car.get('year', '')}

📊 *Орієнтовні річні витрати:*

🔧 Обслуговування: ~{{maintenance}} €  
⛽ Паливо (12 тис. км): ~{{fuel}} €  
🧾 Податки + страхування: ~{{taxes_insurance}} €  
📉 Амортизація (зниження вартості): ~{{depreciation_year}} €  

💰 *Разом: приблизно {{total}} € / рік*  
(≈ {{monthly}} € / місяць)

---

💥 *Типові поломки та надійність:*

- Вкажи 3–5 найтиповіших проблем для цієї моделі, для кожної вкажи середню вартість ремонту у {country} (наприклад: "Підвіска — ~300 €", "АКПП — ~1200 €", "Ланцюг ГРМ — ~400 €").
- Оціни загальну надійність авто за 5-бальною шкалою.

---

♻️ *Екологічні обмеження:*

✅ Вкажи, чи є обмеження для цього авто у {country} (наприклад, дизелі, бензин, гібриди тощо).

---

💳 *Порівняння з іншими варіантами:*

- 🚗 Витрати на авто: ~{{monthly}} €/міс.  
- 🚍 Місячний проїзний на транспорт: ~{{public_transport}} €  
- 💡 Економія при відмові від авто: ~{{saving}} €/рік

---

📈 *Попит на цю модель у {country}:*

- Опиши коротко, який попит на цю модель зараз у {country} (наприклад: "Попит середній, модель популярна серед сімей", або "Попит низький через дорогий ремонт").

"""
    client = openai.AsyncOpenAI(api_key=OPENAI_API_KEY)
    response = await client.chat.completions.create(
        model="gpt-4o",
        messages=[{"role": "user", "content": prompt}],
        max_tokens=900,
        temperature=0.7,
    )
    return response.choices[0].message.content.strip()